package a;

public class Primitives {

	public static void main(String[] args) {
		
		// each variable has:
		// 1. type
		// 2. identifier (name)
		// 3. value
		
		// integers
		byte n1 = 10;
		short n2 = 100;
		int n3 = 1_000;
		long n4 = 100L;
		
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n3);
		System.out.println(n4);
		System.out.println("===============");
		
		// decimals
		float f1 = 5.3F;
		double f2 = 5.32;
		
		System.out.println(f1);
		System.out.println(f2);
		System.out.println("===============");
		
		
		System.out.println(5);
		System.out.println(5D);
		
		System.out.println("===============");
		
		System.out.println(3.3333333333333333333333333333333333333333333333333D);
		System.out.println(3.3333333333333333333333333333333333333333333333333F);
		

	}

}
