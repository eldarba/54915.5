package b.loops;

public class Demo5For {

	public static void main(String[] args) {

		int val = 5;
		val = val + 3;
		val += 3;
		val += 1;

		val++;
		val = val + 1;

		val--;
		val = val - 1;

		// for loop
		for (int i = 1; i <= 5; i++) {
			System.out.println("hello");
		}

	}

}
